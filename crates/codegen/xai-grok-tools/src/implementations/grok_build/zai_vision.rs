#[derive(Clone)]
pub struct ZaiVisionClient {
    auth_provider: SharedApiKeyProvider,
    doctor: Arc<tokio::sync::OnceCell<ZaiVisionDoctorReport>>,
}

impl std::fmt::Debug for ZaiVisionClient {
    fn fmt(&self, formatter: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        formatter
            .debug_struct("ZaiVisionClient")
            .finish_non_exhaustive()
    }
}

impl ZaiVisionClient {
    pub fn new(auth_provider: SharedApiKeyProvider) -> Result<Self, xai_tool_runtime::ToolError> {
        if auth_provider.request_auth_provider_id() != Some(ZAI_CODING_PLAN_PROVIDER_ID) {
            return Err(vision_error(
                "zai_vision_authentication",
                "Z.AI Coding Plan authentication is required for Vision MCP",
            ));
        }
        Ok(Self {
            auth_provider,
            doctor: Arc::new(tokio::sync::OnceCell::new()),
        })
    }

    pub async fn doctor(&self) -> Result<ZaiVisionDoctorReport, xai_tool_runtime::ToolError> {
        self.doctor.get_or_try_init(run_doctor).await.cloned()
    }

    /// Analyze a shell-persisted user attachment. This path is generated by
    /// the session and already resides in its private assets directory.
    pub async fn analyze_attachment(
        &self,
        image_path: &Path,
        prompt: &str,
    ) -> Result<String, xai_tool_runtime::ToolError> {
        validate_prompt(prompt)?;
        let source = validate_generated_attachment(image_path, MediaKind::Image)?;
        let result = self
            .call_mcp_tool(
                "analyze_image",
                serde_json::json!({"image_source": source, "prompt": prompt}),
            )
            .await?;
        mcp_text_content(&result)
    }

    async fn call_mcp_tool(
        &self,
        expected_tool: &str,
        arguments: serde_json::Value,
    ) -> Result<serde_json::Value, xai_tool_runtime::ToolError> {
        let _ = self.doctor().await?;
        let auth = crate::types::resolve_zai_coding_plan_request_auth(&self.auth_provider)
            .await
            .map_err(|_| {
                vision_error(
                    "zai_vision_authentication",
                    "Z.AI Coding Plan authentication is required for Vision MCP",
                )
            })?;
        let bearer = auth.bearer_token().map_err(|_| {
            vision_error(
                "zai_vision_authentication",
                "Z.AI Coding Plan authentication is required for Vision MCP",
            )
        })?;

        let isolated_home = tempfile::tempdir().map_err(|_| {
            vision_error(
                "zai_vision_unavailable",
                "Z.AI Vision MCP could not create an isolated runtime directory",
            )
        })?;
        let mut command = tokio::process::Command::new("npx");
        command
            .args(["--yes", ZAI_VISION_MCP_PACKAGE])
            .env_clear()
            .env("HOME", isolated_home.path())
            .stdin(Stdio::piped())
            .stdout(Stdio::piped())
            .stderr(Stdio::null())
            .kill_on_drop(true);
        copy_safe_child_environment(&mut command);
        command.env("Z_AI_API_KEY", bearer).env("Z_AI_MODE", "ZAI");
        new_process_group(&mut command);

        let mut child = command.spawn().map_err(|_| {
            vision_error(
                "zai_vision_unavailable",
                "Z.AI Vision MCP could not start; run zai_vision_doctor",
            )
        })?;
        let mut process_group = ProcessGroup::new().map_err(|_| {
            vision_error(
                "zai_vision_unavailable",
                "Z.AI Vision MCP process isolation is unavailable",
            )
        })?;
        process_group.attach(&child).map_err(|_| {
            vision_error(
                "zai_vision_unavailable",
                "Z.AI Vision MCP process isolation is unavailable",
            )
        })?;
        let stdin = child.stdin.take().ok_or_else(|| {
            vision_error(
                "zai_vision_protocol",
                "Z.AI Vision MCP stdio transport is unavailable",
            )
        })?;
        let stdout = child.stdout.take().ok_or_else(|| {
            vision_error(
                "zai_vision_protocol",
                "Z.AI Vision MCP stdio transport is unavailable",
            )
        })?;
        let mut child = VisionChild {
            child,
            process_group,
            _isolated_home: isolated_home,
        };
        let mut stdin = stdin;
        let mut stdout = BufReader::new(stdout.take((MAX_STDIO_BYTES + 1) as u64));

        let exchange = exchange_vision_mcp(&mut stdout, &mut stdin, expected_tool, arguments);
        let result = tokio::time::timeout(MCP_TIMEOUT, exchange)
            .await
            .map_err(|_| vision_error("zai_vision_timeout", "Z.AI Vision MCP timed out"))?;
        child.terminate();
        result
    }
}

struct VisionChild {
    child: tokio::process::Child,
    process_group: ProcessGroup,
    _isolated_home: tempfile::TempDir,
}

impl VisionChild {
    fn terminate(&mut self) {
        let _ = self.process_group.kill();
        let _ = self.child.start_kill();
    }
}

impl Drop for VisionChild {
    fn drop(&mut self) {
        self.terminate();
    }
}

async fn run_doctor() -> Result<ZaiVisionDoctorReport, xai_tool_runtime::ToolError> {
    let node_version = command_version("node").await.map_err(|_| {
        vision_error(
            "zai_vision_node_missing",
            "Z.AI Vision MCP requires Node.js 22 or newer",
        )
    })?;
    let major = node_version
        .trim_start_matches('v')
        .split('.')
        .next()
        .and_then(|value| value.parse::<u64>().ok())
        .ok_or_else(|| {
            vision_error(
                "zai_vision_node_version",
                "Z.AI Vision MCP could not determine the Node.js version",
            )
        })?;
    if major < 22 {
        return Err(vision_error(
            "zai_vision_node_version",
            "Z.AI Vision MCP requires Node.js 22 or newer",
        ));
    }
    let npx_version = command_version("npx").await.map_err(|_| {
        vision_error(
            "zai_vision_npx_missing",
            "Z.AI Vision MCP requires npx on PATH",
        )
    })?;
    Ok(ZaiVisionDoctorReport {
        node_version,
        npx_version,
        package_version: ZAI_VISION_MCP_PACKAGE_VERSION,
        package_integrity: ZAI_VISION_MCP_PACKAGE_INTEGRITY,
    })
}

async fn command_version(program: &str) -> Result<String, ()> {
    let mut command = tokio::process::Command::new(program);
    command
        .arg("--version")
        .env_clear()
        .stdin(Stdio::null())
        .stdout(Stdio::piped())
        .stderr(Stdio::null())
        .kill_on_drop(true);
    copy_safe_child_environment(&mut command);
    let mut child = command.spawn().map_err(|_| ())?;
    let stdout = child.stdout.take().ok_or(())?;
    let read = async {
        let mut bytes = Vec::new();
        stdout
            .take(257)
            .read_to_end(&mut bytes)
            .await
            .map_err(|_| ())?;
        let status = child.wait().await.map_err(|_| ())?;
        if !status.success() || bytes.is_empty() || bytes.len() > 256 {
            return Err(());
        }
        let value = std::str::from_utf8(&bytes).map_err(|_| ())?.trim();
        if value.is_empty()
            || value.len() > 64
            || !value.chars().all(|character| {
                character.is_ascii_alphanumeric() || matches!(character, '.' | '-' | '+')
            })
        {
            return Err(());
        }
        Ok(value.to_owned())
    };
    tokio::time::timeout(DOCTOR_TIMEOUT, read)
        .await
        .map_err(|_| ())?
}

fn copy_safe_child_environment(command: &mut tokio::process::Command) {
    for name in [
        "PATH",
        "TMPDIR",
        "TMP",
        "TEMP",
        "SYSTEMROOT",
        "SystemRoot",
        "PATHEXT",
    ] {
        if let Some(value) = std::env::var_os(name) {
            command.env(name, value);
        }
    }
}

async fn write_json_line<W: tokio::io::AsyncWrite + Unpin>(
    writer: &mut W,
    value: &serde_json::Value,
) -> Result<(), xai_tool_runtime::ToolError> {
    let bytes = serde_json::to_vec(value).map_err(|_| invalid_vision_response())?;
    if bytes.len() > MAX_STDIO_BYTES {
        return Err(vision_error(
            "zai_vision_request_too_large",
            "Z.AI Vision MCP request exceeds the local transport limit",
        ));
    }
    writer
        .write_all(&bytes)
        .await
        .map_err(|_| invalid_vision_response())?;
    writer
        .write_all(b"\n")
        .await
        .map_err(|_| invalid_vision_response())?;
    writer.flush().await.map_err(|_| invalid_vision_response())
}

async fn read_jsonrpc_response<R: tokio::io::AsyncBufRead + Unpin>(
    reader: &mut R,
    expected_id: i64,
) -> Result<serde_json::Value, xai_tool_runtime::ToolError> {
    loop {
        let mut line = String::new();
        let read = reader
            .read_line(&mut line)
            .await
            .map_err(|_| invalid_vision_response())?;
        if read == 0 || line.len() > MAX_STDIO_BYTES {
            return Err(invalid_vision_response());
        }
        let value: serde_json::Value =
            serde_json::from_str(line.trim()).map_err(|_| invalid_vision_response())?;
        if value.get("id").and_then(serde_json::Value::as_i64) != Some(expected_id) {
            continue;
        }
        if value.get("jsonrpc").and_then(serde_json::Value::as_str) != Some("2.0")
            || value.get("error").is_some()
        {
            return Err(vision_error(
                "zai_vision_rejected",
                "Z.AI Vision MCP rejected the request",
            ));
        }
        return Ok(value);
    }
}

async fn exchange_vision_mcp<R, W>(
    reader: &mut R,
    writer: &mut W,
    expected_tool: &str,
    arguments: serde_json::Value,
) -> Result<serde_json::Value, xai_tool_runtime::ToolError>
where
    R: tokio::io::AsyncBufRead + Unpin,
    W: tokio::io::AsyncWrite + Unpin,
{
    write_json_line(
        writer,
        &serde_json::json!({
            "jsonrpc": "2.0",
            "id": 1,
            "method": "initialize",
            "params": {
                "protocolVersion": MCP_PROTOCOL_VERSION,
                "capabilities": {},
                "clientInfo": {
                    "name": "grok-build-enhanced",
                    "version": env!("CARGO_PKG_VERSION")
                }
            }
        }),
    )
    .await?;
    let initialized = read_jsonrpc_response(reader, 1).await?;
    let protocol = initialized
        .pointer("/result/protocolVersion")
        .and_then(serde_json::Value::as_str)
        .ok_or_else(invalid_vision_response)?;
    if !matches!(protocol, "2024-11-05" | "2025-03-26" | "2025-06-18") {
        return Err(vision_error(
            "zai_vision_protocol",
            "Z.AI Vision MCP negotiated an unsupported protocol version",
        ));
    }

    write_json_line(
        writer,
        &serde_json::json!({
            "jsonrpc": "2.0",
            "method": "notifications/initialized",
            "params": {}
        }),
    )
    .await?;
    write_json_line(
        writer,
        &serde_json::json!({
            "jsonrpc": "2.0",
            "id": 2,
            "method": "tools/list",
            "params": {}
        }),
    )
    .await?;
    let listed = read_jsonrpc_response(reader, 2).await?;
    let is_listed = listed
        .pointer("/result/tools")
        .and_then(serde_json::Value::as_array)
        .is_some_and(|tools| {
            tools.iter().any(|tool| {
                tool.get("name").and_then(serde_json::Value::as_str) == Some(expected_tool)
            })
        });
    if !is_listed {
        return Err(vision_error(
            "zai_vision_protocol",
            "The pinned Z.AI Vision MCP package does not expose the requested tool",
        ));
    }

    write_json_line(
        writer,
        &serde_json::json!({
            "jsonrpc": "2.0",
            "id": 3,
            "method": "tools/call",
            "params": {"name": expected_tool, "arguments": arguments}
        }),
    )
    .await?;
    let called = read_jsonrpc_response(reader, 3).await?;
    let result = called
        .get("result")
        .cloned()
        .ok_or_else(invalid_vision_response)?;
    if result.get("isError").and_then(serde_json::Value::as_bool) == Some(true) {
        return Err(vision_error(
            "zai_vision_rejected",
            "Z.AI Vision MCP rejected the media analysis request",
        ));
    }
    Ok(result)
}

fn mcp_text_content(result: &serde_json::Value) -> Result<String, xai_tool_runtime::ToolError> {
    let content = result
        .get("content")
        .and_then(serde_json::Value::as_array)
        .ok_or_else(invalid_vision_response)?;
    let mut text = String::new();
    for block in content {
        if block.get("type").and_then(serde_json::Value::as_str) == Some("text")
            && let Some(value) = block.get("text").and_then(serde_json::Value::as_str)
        {
            if !text.is_empty() {
                text.push('\n');
            }
            text.push_str(value);
        }
    }
    if text.trim().is_empty() {
        return Err(invalid_vision_response());
    }
    Ok(text)
}

fn invalid_vision_response() -> xai_tool_runtime::ToolError {
    vision_error(
        "zai_vision_protocol",
        "Z.AI Vision MCP returned an invalid response",
    )
}

fn vision_error(code: &'static str, message: &'static str) -> xai_tool_runtime::ToolError {
    xai_tool_runtime::ToolError::custom(code, message)
}

#[derive(Clone, Copy)]
enum MediaKind {
    Image,
    Video,
}

fn validate_generated_attachment(
    path: &Path,
    kind: MediaKind,
) -> Result<String, xai_tool_runtime::ToolError> {
    let canonical = std::fs::canonicalize(path).map_err(|_| invalid_media_source(kind))?;
    validate_local_media(&canonical, kind)?;
    Ok(canonical.to_string_lossy().into_owned())
}

fn validate_media_source(
    source: &str,
    kind: MediaKind,
    cwd: &Path,
    session_folder: &Path,
) -> Result<String, xai_tool_runtime::ToolError> {
    let source = source.trim();
    if source.is_empty() || source.len() > 4_096 {
        return Err(invalid_media_source(kind));
    }
    // The local MCP process must never resolve or fetch model-supplied URLs.
    // Users can first download public media through the SSRF-safe WebFetch
    // path, then pass the resulting workspace/session file to Vision.
    if source.contains("://") {
        return Err(invalid_media_source(kind));
    }

    let candidate = PathBuf::from(source);
    let candidate = if candidate.is_absolute() {
        candidate
    } else {
        cwd.join(candidate)
    };
    let canonical = std::fs::canonicalize(candidate).map_err(|_| invalid_media_source(kind))?;
    let cwd = std::fs::canonicalize(cwd).map_err(|_| invalid_media_source(kind))?;
    let session = std::fs::canonicalize(session_folder).map_err(|_| invalid_media_source(kind))?;
    if !canonical.starts_with(&cwd) && !canonical.starts_with(&session) {
        return Err(vision_error(
            "zai_vision_path_denied",
            "Vision MCP local media must be inside the workspace or session folder",
        ));
    }
    validate_local_media(&canonical, kind)?;
    Ok(canonical.to_string_lossy().into_owned())
}

fn validate_local_media(
    canonical: &Path,
    kind: MediaKind,
) -> Result<(), xai_tool_runtime::ToolError> {
    validate_extension(canonical.to_string_lossy().as_ref(), kind)?;
    let metadata = std::fs::metadata(canonical).map_err(|_| invalid_media_source(kind))?;
    let max_bytes = match kind {
        MediaKind::Image => MAX_IMAGE_BYTES,
        MediaKind::Video => MAX_VIDEO_BYTES,
    };
    if !metadata.is_file() || metadata.len() == 0 || metadata.len() > max_bytes {
        return Err(invalid_media_source(kind));
    }
    Ok(())
}

fn validate_extension(source: &str, kind: MediaKind) -> Result<(), xai_tool_runtime::ToolError> {
    let extension = Path::new(source)
        .extension()
        .and_then(|extension| extension.to_str())
        .map(str::to_ascii_lowercase)
        .ok_or_else(|| invalid_media_source(kind))?;
    let valid = match kind {
        MediaKind::Image => matches!(
            extension.as_str(),
            "png" | "jpg" | "jpeg" | "gif" | "webp" | "bmp"
        ),
        MediaKind::Video => matches!(extension.as_str(), "mp4" | "mov" | "m4v"),
    };
    valid
        .then_some(())
        .ok_or_else(|| invalid_media_source(kind))
}

fn invalid_media_source(kind: MediaKind) -> xai_tool_runtime::ToolError {
    match kind {
        MediaKind::Image => vision_error(
            "zai_vision_invalid_image",
            "Vision MCP requires a supported image inside the workspace or session folder",
        ),
        MediaKind::Video => vision_error(
            "zai_vision_invalid_video",
            "Vision MCP requires an MP4, MOV, or M4V up to 8 MiB inside the workspace or session folder",
        ),
    }
}

fn validate_prompt(prompt: &str) -> Result<(), xai_tool_runtime::ToolError> {
    if prompt.trim().is_empty() || prompt.chars().count() > MAX_PROMPT_CHARS {
        return Err(xai_tool_runtime::ToolError::invalid_arguments(format!(
            "prompt must contain between 1 and {MAX_PROMPT_CHARS} characters"
        )));
    }
    Ok(())
}

async fn vision_context(
    ctx: &xai_tool_runtime::ToolCallContext,
) -> Result<(ZaiVisionClient, PathBuf, PathBuf), xai_tool_runtime::ToolError> {
    let resources = shared_resources(ctx)?;
    let resources = resources.lock().await;
    Ok((
        resources.require::<ZaiVisionClient>()?.clone(),
        resources.require::<Cwd>()?.0.clone(),
        resources.require::<SessionFolder>()?.0.clone(),
    ))
}

async fn run_image_tool(
    ctx: xai_tool_runtime::ToolCallContext,
    provider_tool: &'static str,
    mut arguments: serde_json::Value,
    source_fields: &[&str],
) -> Result<ToolOutput, xai_tool_runtime::ToolError> {
    let prompt = arguments
        .get("prompt")
        .and_then(serde_json::Value::as_str)
        .ok_or_else(|| xai_tool_runtime::ToolError::invalid_arguments("prompt is required"))?;
    validate_prompt(prompt)?;
    let (client, cwd, session_folder) = vision_context(&ctx).await?;
    for field in source_fields {
        let source = arguments
            .get(*field)
            .and_then(serde_json::Value::as_str)
            .ok_or_else(|| {
                xai_tool_runtime::ToolError::invalid_arguments(format!("{field} is required"))
            })?;
        let source = validate_media_source(source, MediaKind::Image, &cwd, &session_folder)?;
        arguments
            .as_object_mut()
            .expect("typed Vision arguments serialize as an object")
            .insert((*field).to_owned(), serde_json::Value::String(source));
    }
    let result = client.call_mcp_tool(provider_tool, arguments).await?;
    Ok(ToolOutput::Text(mcp_text_content(&result)?.into()))
}

async fn run_video_tool(
    ctx: xai_tool_runtime::ToolCallContext,
    mut arguments: serde_json::Value,
) -> Result<ToolOutput, xai_tool_runtime::ToolError> {
    let prompt = arguments
        .get("prompt")
        .and_then(serde_json::Value::as_str)
        .ok_or_else(|| xai_tool_runtime::ToolError::invalid_arguments("prompt is required"))?;
    validate_prompt(prompt)?;
    let (client, cwd, session_folder) = vision_context(&ctx).await?;
    let source = arguments
        .get("video_source")
        .and_then(serde_json::Value::as_str)
        .ok_or_else(|| {
            xai_tool_runtime::ToolError::invalid_arguments("video_source is required")
        })?;
    let source = validate_media_source(source, MediaKind::Video, &cwd, &session_folder)?;
    arguments
        .as_object_mut()
        .expect("typed Vision arguments serialize as an object")
        .insert("video_source".to_owned(), serde_json::Value::String(source));
    let result = client.call_mcp_tool("analyze_video", arguments).await?;
    Ok(ToolOutput::Text(mcp_text_content(&result)?.into()))
}

#[derive(Debug, Clone, serde::Serialize, serde::Deserialize, schemars::JsonSchema)]
