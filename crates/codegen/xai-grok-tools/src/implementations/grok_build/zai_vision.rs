//! Opt-in Z.AI Coding Plan Vision MCP integration.
//!
//! The provider package runs as a short-lived, bounded stdio MCP subprocess.
//! Its exact package version is pinned, the child receives only a minimal
//! environment plus the dynamically resolved Z.AI credential, and local media
//! paths are canonicalized beneath the workspace or session directory before
//! they cross the process boundary.

