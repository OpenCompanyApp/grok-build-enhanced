macro_rules! vision_tool_metadata {
    ($tool:ty, $description:expr) => {
        impl ToolMetadata for $tool {
            fn kind(&self) -> ToolKind {
                ToolKind::Read
            }
            fn tool_namespace(&self) -> ToolNamespace {
                ToolNamespace::GrokBuild
            }
            fn description_template(&self) -> &str {
                $description
            }
            fn requires_expr(&self) -> Expr<ToolRequirement> {
                Expr::True
            }
        }
    };
}

macro_rules! vision_tool_common {
    ($name:expr, $description:expr) => {
        fn id(&self) -> xai_tool_protocol::ToolId {
            xai_tool_protocol::ToolId::new($name).expect("valid tool id")
        }
        fn description(
            &self,
            _ctx: &xai_tool_runtime::ListToolsContext,
        ) -> xai_tool_types::ToolDescription {
            xai_tool_types::ToolDescription::new($name, $description)
        }
        fn capabilities(&self) -> xai_tool_protocol::ToolCapabilities {
            xai_tool_protocol::ToolCapabilities {
                is_read_only: true,
                tool_scope: Some(xai_tool_protocol::ToolScope::Read),
                ..Default::default()
            }
        }
    };
}

